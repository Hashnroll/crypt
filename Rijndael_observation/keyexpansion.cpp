void rot_word(word8 key_word[4]) {
	char first_byte_of_word = key_word[0];
	for (int i = 0; i < 3; ++i)
		key_word[i] = key_word[i + 1];
	key_word[3] = first_byte_of_word;
}

void sub_word(word8 key_word[4]) {
	for (int i = 0; i < 4; ++i)
		key_word[i] = S[key_word[i]];
}

void word_xor(word8 temp[4], word8 w2[4]) {
	for (int i = 0; i < 4; ++i)
		temp[i] ^= w2[i];
}

void keyexpansion(unsigned char key[4*Nk], word8 keywords[4][kw_size]) {
	int i;
	for (i = 0; i < Nk; ++i)
		for (int k = 0; k < 4; ++k)
			keywords[k][i] = key[4 * i + k];
	while (i < kw_size) {
		word8 temp[4];
		for (int j = 0; j < 4; ++j)
			temp[j] = keywords[j][i-1];
		if (i%Nk == 0) {
			rot_word(temp);
			sub_word(temp);
			word8 rcon_word[4] = { rcon[i / Nk],0,0,0 };
			word_xor(temp, rcon_word);
		}
		else if ((Nk > 6) && (i%Nk == Nb))
			sub_word(temp);
		word8 x[4];
		for (int k = 0; k < 4; ++k) 
			x[k] = keywords[k][i - Nk];
		word_xor(temp, x);
		for (int j = 0; j < 4; ++j)
			keywords[j][i] = temp[j];
		++i;
	}
}

